-- ---------------------------------------------------------------------------
-- Realtime Broadcast (invalidation bus): policies on realtime.messages
-- ---------------------------------------------------------------------------
-- Authenticated users may receive (subscribe to) broadcast only. INSERT/send is not granted here:
-- the service_role key bypasses RLS, so API/workers publish via getSupabaseServiceClient() only.
-- Chat authorization stays in the HTTP API; invalidation hints may be observable (accepted tradeoff).
-- Use private channels in the client (`private: true`) and disable Realtime "Allow public access"
-- so only authenticated JWTs can attach (anon has no policies here).
-- Upgrade: if you had INSERT for authenticated, run:
--   DROP POLICY IF EXISTS "prsn_realtime_broadcast_insert" ON realtime.messages;

CREATE POLICY "prsn_realtime_broadcast_select"
	ON realtime.messages
	FOR SELECT
	TO authenticated
	USING (realtime.messages.extension = 'broadcast');

COMMENT ON POLICY "prsn_realtime_broadcast_select" ON realtime.messages IS 'Parascene: authenticated may receive broadcast; publish via service_role only.';
